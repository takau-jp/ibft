#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	unsigned int	size;

	size = 0;
	while (lst)
	{
		lst = lst->next;
		size++;
		if (size > INT_MAX)
			return (-1);
	}
	return (size);
}
